package com.itheima;

public class ExtendsDemo3 {
	
	public static void main(String[] args) {
		newphone np = new newphone();
		np.call();
	}
}
class phone{
	public void call(){
		System.out.println("你好呀");
	}
}

class newphone extends phone{
	//@Override
	public void call(){
		System.out.println("哈哈");
		super.call();
	}
}